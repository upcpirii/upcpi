Route::group(['middleware' => 'auth.basic.once'], function () {
    $this->get('/user', function (Request $request) {
        return $request->user();
    });

    $this->group(['namespace' => 'Member'], function () {
        $this->post('/members', 'Controller@store');
    });

    $this->post('/families', 'FamilyController@store');
});
